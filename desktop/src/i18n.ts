type TranslationPrimitive = string | number | boolean | null;
type TranslationValue =
  | TranslationPrimitive
  | TranslationMap
  | TranslationValue[];

interface TranslationMap {
  [key: string]: TranslationValue;
}

function isTranslationMap(value: unknown): value is TranslationMap {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function deepMergeTranslations(
  base: TranslationMap,
  overrides: TranslationMap,
): TranslationMap {
  const result: TranslationMap = { ...base };

  for (const [key, value] of Object.entries(overrides)) {
    const existing = result[key];
    if (isTranslationMap(existing) && isTranslationMap(value)) {
      result[key] = deepMergeTranslations(existing, value);
      continue;
    }

    // Keep nested Web message groups when legacy resources only provide a flat
    // primitive at the same top-level key (for example `export` or `translate`).
    if (isTranslationMap(existing) && !isTranslationMap(value)) {
      continue;
    }

    result[key] = value;
  }

  return result;
}

