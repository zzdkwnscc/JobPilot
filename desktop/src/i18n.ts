function buildTranslation(
  webMessages: TranslationMap,
  legacyTranslation: TranslationMap,
  nestedCompat: TranslationMap,
): TranslationMap {
  const merged = deepMergeTranslations(
    deepMergeTranslations(webMessages, nestedCompat),
    legacyTranslation,
  );

  if (typeof legacyTranslation.editorUntitle === "string") {
    merged.editorUntitled = legacyTranslation.editorUntitle;
  }

  return merged;
}

